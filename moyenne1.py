# une fonction qui permet de calculer la moyenne 
note1 = int(input("Saisir la première note"))
note2 = int(input("Saisir la seconde note"))
note3 = int(input("Saisir la troisième note"))

# Calcul du resultat
resultat = (note1 + note2 + note3) / 3

# Affichage du resultat
print("la moyenne est " ,resultat)
